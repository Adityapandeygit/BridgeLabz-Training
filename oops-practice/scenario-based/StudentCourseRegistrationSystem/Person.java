package oops_practice.scanrio_based.StudentCourseRegistrationSystem;

public class Person {
	private int id;
    private String name;
    public Person(int id, String name) {
        this.id = id;
        this.name = name;
    }
    public void setId() {
    	this.id = id;
    }
    public int getId() {
    	return id ;
    }
    public void setName() {
    	this.name = name;
    }
    public String getName() {
    	return name;
    }
}
